<p>To reach the current and future goal, we follow a three-pronged approach pivoted on our core values:</p>

<p>First, we pursue &ldquo;excellence in quality&rdquo;. We integrate quality into every process in the company, both through continuously seeking international standards, certification and through implanting quality as part of the company culture. This introduces levels of optimization and efficiency that cannot be achieved through traditional means.</p>

<p>Second, Orchidia&rsquo;s mission is guided by &ldquo;Improving peoples&rsquo; lives&rdquo; as the heart of its success.</p>

<p>This can be seen in the company&rsquo;s continuous interest in employee improvement and advancement. But, perhaps, more importantly, a sense of corporate and social responsibility is integral to company culture.</p>

<p>Third, the company considers &ldquo;Customer Focus, Innovation &amp; Creativity, Integrity &amp; Transparency,&rdquo; to be part of its values. This is translated into openness, transparency, and accountability at all levels and in all stages in the production process. It is also reflected in the R&amp;D pipeline which is designed to keep up with the demands of the market as well as patient Well-being.</p>

<p>One of our main strategies for the coming period is a focus on export markets. This area is currently seeing huge growth with multiple new markets being opened every year. This stage offers both significant challenges as well as unprecedented opportunities and is already proving to be a major engine of company growth.</p>


